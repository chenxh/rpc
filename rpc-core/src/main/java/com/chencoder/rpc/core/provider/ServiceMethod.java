package com.chencoder.rpc.core.provider;

import java.util.Arrays;

public class ServiceMethod {
	
    private String serviceName;

    private String methodName;

    private Class<?>[] paramTypes;
    
    public ServiceMethod(String serviceName,String methodName){
    	this(serviceName, methodName, null);
    }
    
    public ServiceMethod(String serviceName,String methodName, Class<?>[] paramTypes ){
    	setServiceName(serviceName);
    	setMethodName(methodName);
    	setParamTypes(paramTypes);
    }

	public String getServiceName() {
		return serviceName;
	}

	public void setServiceName(String serviceName) {
		this.serviceName = serviceName;
	}

	public String getMethodName() {
		return methodName;
	}

	public void setMethodName(String methodName) {
		this.methodName = methodName;
	}

	public Class[] getParamTypes() {
		return paramTypes;
	}

	public void setParamTypes(Class[] paramTypes) {
		this.paramTypes = paramTypes;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((methodName == null) ? 0 : methodName.hashCode());
		result = prime * result + Arrays.hashCode(paramTypes);
		result = prime * result + ((serviceName == null) ? 0 : serviceName.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		ServiceMethod other = (ServiceMethod) obj;
		if (methodName == null) {
			if (other.methodName != null)
				return false;
		} else if (!methodName.equals(other.methodName))
			return false;
		if (!Arrays.equals(paramTypes, other.paramTypes))
			return false;
		if (serviceName == null) {
			if (other.serviceName != null)
				return false;
		} else if (!serviceName.equals(other.serviceName))
			return false;
		return true;
	}
    
    
    

}
